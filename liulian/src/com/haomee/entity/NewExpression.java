package com.haomee.entity;

public class NewExpression {

	private String packageName;
	private String expressionId;
	private int local_url;
	private String expressionUrl;
	private String expressionName;
	private String expressionWidth;
	private String expressionHeight;

	public String getPackageName() {
		return packageName;
	}

	public void setPackageName(String packageName) {
		this.packageName = packageName;
	}

	public String getExpressionId() {
		return expressionId;
	}

	public void setExpressionId(String expressionId) {
		this.expressionId = expressionId;
	}

	public int getLocal_url() {
		return local_url;
	}

	public void setLocal_url(int local_url) {
		this.local_url = local_url;
	}

	public String getExpressionUrl() {
		return expressionUrl;
	}

	public void setExpressionUrl(String expressionUrl) {
		this.expressionUrl = expressionUrl;
	}

	public String getExpressionName() {
		return expressionName;
	}

	public void setExpressionName(String expressionName) {
		this.expressionName = expressionName;
	}

	public String getExpressionWidth() {
		return expressionWidth;
	}

	public void setExpressionWidth(String expressionWidth) {
		this.expressionWidth = expressionWidth;
	}

	public String getExpressionHeight() {
		return expressionHeight;
	}

	public void setExpressionHeight(String expressionHeight) {
		this.expressionHeight = expressionHeight;
	}

}
