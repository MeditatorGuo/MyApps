	public void setIs_answer(boolean is_answer) {
		this.is_answer = is_answer;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getLeft_id() {
		return left_id;
	}
	public void setLeft_id(String left_id) {
		this.left_id = left_id;
	}
	public String getRight_id() {
		return right_id;
	}
	public void setRight_id(String right_id) {
		this.right_id = right_id;
	}
	public String getLeft_title() {
		return left_title;
	}
	public void setLeft_title(String left_title) {
		this.left_title = left_title;
	}
	public String getRight_title() {
		return right_title;
	}
	public void setRight_title(String right_title) {
		this.right_title = right_title;
	}
	public String getLeft_num() {
		return left_num;
	}
	public void setLeft_num(String left_num) {
		this.left_num = left_num;
	}
	public String getRight_num() {
		return right_num;
	}
	public void setRight_num(String right_num) {
		this.right_num = right_num;
	}
	
